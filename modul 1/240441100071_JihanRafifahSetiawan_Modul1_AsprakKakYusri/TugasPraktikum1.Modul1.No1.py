#Soal Nomor 1
#Tulis Ukuran Balok
panjang = 20
lebar = 13
tinggi = 7
#Hasil Perhitungan Volume Balok
volume_balok = panjang * lebar * tinggi
print("Hasil dari perhitungan volume balok adalah", volume_balok)
#Tulis Ukuran Tabung
diameter = 14  # cm
radius = diameter / 2
luas_selimut = 440  # cm^2
#Menghitung Tinggi Tabung Menggunakan Luas Selimut Tabung
pi = 3.14
tinggi_tabung = luas_selimut / (2 * pi * radius)
#Hasil Perhitungan Volume Tabung
volume_tabung = pi * radius ** 2 * tinggi_tabung
print("Hasil dari perhitungan volume tabung adalah", volume_tabung)